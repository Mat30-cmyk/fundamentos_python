# Literales en Python

# Cadenas vs números
print("2")   # cadena
print(2)     # entero

# Enteros
print(123)
print(-100)
print(11_111_111)

# Octal y Hexadecimal
print(0o123)   # octal → 83
print(0x123)   # hexadecimal → 291

# Flotantes
print(2.5)
print(-0.4)
print(.4)
print(4.)

# Notación científica
print(3E8)
print(6.62607E-34)

# Cadenas con comillas
print("Me gusta \"Monty Python\"")
print('Me gusta "Monty Python"')
print('I\'m Monty Python.')

# Booleanos
print(True)
print(False)
print(True > False)
print(True < False)

# None
print(None)

